#Escape sequences in print - "\n", "\t"
print("Line1\nLine2")             # New Line
print("Item1\tItem2")             # Tab space
print("He said, \"Hello\"")       # Quoted string
print('It\'s Python!')            #Apostrophe in single quote

# Newline (\n)
print("Welcome to Python\nLet's learn together!")

# Tab (\t)
print("Name\tAge\tLocation")
print("Aisha\t25\tLagos")

# Quotes inside string
print("She said, \"Hello there!\"")
print('It\'s a sunny day.')

# Backlash
print("File path:\n\nUsers\n\tAisha\t\tDocuments")
print("She\ntalk say\ntime no dey her side o\nsay for her world day dey quick dey dark o\nand I go love am till I die go\nbut our hearts e don turn to stone")
print("@@\n@@@@\n@@@@@@\n@@@@@@@@\n@@@@@@@@@@\n@@@@@@@@@@@@")
print("Come\nLet\tus\nreason\ttogether\nand\tmake\tman,\nin\tour\town\timage")
print("")